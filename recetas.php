							<h3>Postre Francis</h3>
							<br>
							<p><strong>INGREDIENTES PARA LA BASE:</strong></p>

								<p>6 huevos</p>
								<p>150 g de azúcar impalpable</p>
								<p>2 sobres de gelatina sin sabor</p>
								<p>1 taza de crema de leche</p>
								<p>400 g de frutillas limpias</p>
								<p>¼ de cucharadita de colorante rojo FLEIBOR</p>
								<p>1 cucharadita de esencia de frutillas FLEIBOR</p>
								<p>1 cucharada de jugo colado de limón</p>
								<p>½ taza de crema de leche ligeramente batida</p>
								<p> ¼ taza de agua fría</p>
								<p>PARA LA SALSA:</p>
								<p>250 g de frutillas</p>
								<p> gotas de esencia de frutillas FLEIBOR</p>
								<p>3 cucharadas de jugo colado de naranja</p>
								<p> 3 cucharadas de azúcar impalpable</p>
								<br>

							<p><strong>PREPARACIÓN:</strong> Colocar en un bol de acero el jugo de limón con la crema de leche;</p>
							<p>Batir a baño de María hirviendo durante 3 minutos. Adicionar las yemas de a una siempre batiendo, el colorante, la esencia y la gelatina previamente hidratada en el agua fría; batir unos segundos más, retirar y mantener tibia.</p>
							<p>Batir las claras con una pizca de sal hasta punto de nieve; adicionar en forma de lluvia el azúcar impalpable tamizada sin</p>
							<p>Dejar de batir. Unir suavemente con la preparación de yemas y la crema batida.</p>
							<p>Dividir la mezcla en dos y a una incorporarle las frutillas trozadas.</p>
							<p>Untar un molde de 22 cm. de diámetro con aceite de maíz o de almendras. Colocar en el fondo del molde la preparación de frutillas y encima la otra; refrigerar durante 2 horas, o de un día para el otro.</p>
							<p>Desmoldar y terminar con hilos de caramelo.</p>
							<p>Servir con la salsa de frutillas.</p>
							<br>

							<p><strong>SALSA:</strong> Licuar las frutillas con el azúcar impalpable, el jugo de naranja, gotas de esencia de frutillas; colar y emplear.</p>
