							<h3>Bombones de Crema Par&iacute;s</h3>
							<br>
							<p><strong>Ingredientes</strong></p>
							<p>125 CC de crema de leche</p>
							<p>300 g de chocolate cobertura semi –dulce trozado</p>
							<p>1 cucharada de esencia de chocolate Fleibor</p>
							<p>50 g de nueces, avellanas o maní tostado y triturado</p>
							<p>4 cucharadas de coñac</p>
							<p>200 g de chocolate sin templar semi –dulce</p>
							<p>Mitades de almendras, cerezas al marrasquino y cuartos de nueces para decorar</p>
							<br>

							<p><strong>Preparación</strong></p>
							<p>Forrar el interior de un molde rectangular mediano, o un hermético con una lamina doble de papel manteca. Reservar.</p>
							<p>Calentar la crema de leche en una cacerolita preferentemente teflonada, o enlozada. Cuando hierva, retirar del fuego, añadir la cobertura y mezclar continuamente con una cuchara de madera hasta obtener una preparación homogénea. Dejar entibiar.</p>
							<p>Adicionar la esencia de chocolate Fleibor, la fruta seca elegida y el licor.</p>
							<p>Extender la mezcla en el molde preparado, dejándola de aproximadamente de 1 cm. de espesor. Dejar que endurezca en sitio fresco.</p>
							<p>Luego, utilizando cortantes de diversos formatos cotar los bombones a gusto.</p>
							<p>Disolver el chocolate a bañomaria suave, y bañar los bombones de a uno, a medida que se los va apoyando sobre una placa forrada con papel manteca.</p>
							<p>Terminar colocando sobre cada uno antes de que el chocolate endurezca, mitades de almendras, cerezas y nuez. Dejar que se oreen en lugar fresco, o llevar unos minutos a la heladera. Colocar en pirotines de diferentes colores.</p>
