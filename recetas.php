							<h3>Cubierta Wilton Para Tortas</h3>
							<br>
							<p><strong>Ingredientes para Cubierta Wilton (Americana) Para Tortas</strong></p>
							<p>1 cucharada de gelatina sin sabor</p>
							<p>¼ taza de agua fría</p>
							<p>½ taza de glucosa</p>
							<p>1 cucharada de glicerina</p>
							<p>2 cucharadas de esencia de chocolate blanco Fleibor</p>
							<p>2 cucharadas de vegetalina</p>
							<p>Azúcar impalpable tamizada dos veces cantidad necesaria (más o menos 1 Kg. a 1 ½ Kg.)</p>
							<p>Colorantes en pasta Fleibor a elección</p>
							<br>
							<p><strong>Preparación</strong></p>
							<p>Mezclar en un bol la gelatina con el agua fría y dejar en reposo 5 mi¬nutos (debe quedar espesa y cristalina), llevar a bañomaria suave y disolverla.</p>
							<p>Añadir la glu¬cosa, la glicerina y la esencia de chocolate blanco Fleibor. Sumar la vegetalina, y cuan¬do esté casi disuelta retirar del fuego y dejar entibiar.</p>
							<p>Colocar sobre mesa o mármol limpio y bien seco el azúcar impalpable tamizado y hacer un hueco en el centro. Volcar la pri¬mera preparación tibia, y unir con cornet incorporando todo el azúcar impalpable.</p>
							<p>Añadir más azúcar impalpable si es necesario hasta lograr una ma¬sa que no se pegue en las manos. En este momento añadir el colorante Fleibor, elegido. Una vez lograda una masa homogénea, estirar sobre mesa espolvoread a con fécula de maíz y azúcar impalpable mezcladas. El palote debe estar muy limpio y también espolvoreado. Forrar la torta en la forma habitual.</p>
							<br>
							<p><strong>A Saber</strong></p>
							<p>Esta pasta es ideal para forrar tortas elaboradas a base de manteca, por ejemplo la torta galesa para bodas, u otras de esta textura.</p>
							<p>Rinde bastante cantidad, y si no se la emplea enseguida, se la puede guardar en bolsa para freezer y dentro de un hermético. Dura hasta 1 mes. </p>
