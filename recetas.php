							<h3>Rosca Alemana De Rosas</h3>
							<br>
							<p><strong>Ingredientes (Para 2 roscas de 26 cm.)</strong></p>
							<p>850 g de harina común 000</p>
							<p>50 g de levadura de cerveza</p>
							<p>150 g de azúcar</p>
							<p>150 g de manteca blanda</p>
							<p>1 cucharada de esencia de limón Fleibor</p>
							<p>3 huevos</p>
							<p>1 cucharada de esencia de vainilla Fleibor</p>
							<p>250 CC de leche aproximadamente</p>
							<p>Colorante vegetal amarillo liquido Fleibor</p>
							<br>
							<p><strong>Relleno</strong></p>
							<p>½ litro de crema pastelera espesa (ver receta en este sitio)</p>
							<p>150 g de chocolate para taza en trocitos</p>
							<p>100 g de nueces picadas</p>
							<br>
							<p><strong>Preparación</strong></p>
							<p>Tamizar la harina sobre la mesada y formar una corona, en el centro disponer el azúcar, la manteca, la esencia de limón y vainilla Fleibor, sumar la levadura desmenuzada, los huevos, gotas de colorante amarillo Fleibor, y comenzar a mezclar los ingredientes del centro de la corona, a medida que se añade la leche tibia. Amasar muy bien hasta obtener una masa elástica y formar un bollo.</p>
							<p>Lubricar el interior de un bol con rocío vegetal, colocar el bollo, tapar y dejar leudar al doble en sitio templado.</p>
							<p>Luego volcar sobre la mesada enharinada, desgasificar con las manos, y estirar con palote en forma rectangular de 60 x 40 cm. aproximadamente.</p>
							<p>Untar toda la masa con la crema pastelera, salpicar con las nueces, el chocolate y arrollar flojo. Cortar rodajas de 5 cm. de ancho, y dividir en partes parejas.</p>
							<p>Enmantecar bien el interior de dos moldes con tubo central de 26 cm. de diámetro. (Pueden utilizarse los de papeles dobles)</p>
							<p>Comenzar a colocar las porciones de arrollado con el relleno hacia arriba, una al lado de la otra, hasta completar toda una vuelta.</p>
							<p>Tapar y dejar leudar al doble.</p>
							<p>Cocinar en horno moderado de de 35 a 40 minutos. Retirar, dejar que pierda el calor fuerte y desmoldar sobre rejilla.</p>
							<p>Pincelar con almíbar básico. </p>
