							<h3>Torta Galesa Express</h3>
							<br>
							<p><strong>Ingredientes</strong></p>
							<p>250 g de manteca</p>
							<p>200 g de azúcar orgánica o negra</p>
							<p>1 cucharada de esencia cointreau Fleibor</p>
							<p>2 cucharadas de coñac</p>
							<p>4 huevos</p>
							<p>1 cucharada de canela molida</p>
							<p>¼ de cucharadita de nuez moscada molida</p>
							<p>150 g de ciruelas descarozadas en trocitos</p>
							<p>200 g de cerezas al marrasquino en trocitos</p>
							<p>100 g de naranja confitada en trocitos</p>
							<p>150 g de pasa de uva rubia</p>
							<p>100 g de pasas de uva</p>
							<p>100 g de nueces picadas</p>
							<p>225 g de harina común 000</p>
							<p>½ cucharadita de polvo para hornear </p>
							<br>
							<p><strong>Preparación</strong></p>
							<p>Forrar el fondo de un molde de 22 a 24 cm. de diámetro con doble disco de papel manteca, y enmantecar bien</p>
							<p>Batir la manteca punto pomada, adicionar el azúcar y las yemas de a una batiendo bien.</p>
							<p>Aromatizar con la esencia cointreau Fleibor, el coñac y la canela. Añadir las frutas y la harina tamizada con el polvo para hornear.</p>
							<p>Sumar por ultimo las claras batidas a nieve y mezclar suavemente.</p>
							<p>Verter en el molde y cocinar en horno moderado durante 1 hora a 1 hora 15 minutos.</p>
							<p>Retirar y dejar asentar en el molde 20 minutos y luego desmoldar.</p>
							<p>Estando fría, forrar con Cubierta Wilton (ver receta en este sitio) decorar con frutas (cerezas, cáscaras de naranja glaseadas, nueces etc.)</p>
							<p>También se la puede pincelar simplemente con mermelada de damascos o duraznos reducida y mantener envuelta.</p>
							<p>*Envuelta con papel manteca, dura hasta 1 mes.</p>
							<p>*en freezer 6 meses</p>
